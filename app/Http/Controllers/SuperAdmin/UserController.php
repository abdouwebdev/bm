     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $group = Group::where('author_id', Auth::user()->id)->first();
        $userAdmin = User::where('role','admin')->get();
        $userSimple = User::where('role','user')->get();
        $users = User::get();
        $userCount = count($users);
        return view('superadmin.user.index', compact('userAdmin','userSimple','users','userCount','group'));
    }

    /**
     * Create a new user instance after a valid registration.
     *
     * @param  array  $data
     * @return \App\Models\User
     */
    protected function create(Request $request)
    {
        $data = $request->all();
        User::create([
            'name' => $data['name'],
            'username' => $data['name'],
            'email' => $data['email'],
            'password' => Hash::make($data['password']),
            'role' => $data['role'],
        ]);

        return back()->with('success', 'Account successfully updated');
    }

